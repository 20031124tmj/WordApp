function parseEntry(entry) {
  const parts = entry.split('|');
  const phonetic = parts[0];
  const defs = [];
  for (let i = 1; i < parts.length; i++) {
    const part = parts[i].trim();
    if (!part) continue;
    if (/^[a-z]+\.$/.test(part)) {
      if (i + 1 < parts.length && parts[i + 1].trim()) {
        defs.push({ pos: part, meaning: parts[i + 1].trim() });
        i++;
      }
    } else if (defs.length === 0) {
      defs.push({ pos: '', meaning: part });
    }
  }
  return { phonetic, definitions: defs };
}

const D = {};
const rawDict = fs.readFileSync(path.join(__dirname, 'dict_compact.txt'), 'utf-8');
for (const line of rawDict.split('\n')) {
  const trimmed = line.trim();
  if (!trimmed || trimmed.startsWith('#')) continue;
  const eqIdx = trimmed.indexOf('=');
  if (eqIdx === -1) continue;
  const word = trimmed.substring(0, eqIdx).trim().toLowerCase();
  const entry = trimmed.substring(eqIdx + 1).trim();
  D[word] = parseEntry(entry);
}

